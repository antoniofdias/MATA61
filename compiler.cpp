int test_string(int i) {

	if (debugUno) cout << "Testando string\n";
	if (debugUno) cout << currentLine << " " << currentColumn << endl;

	int size = 0;
	int prevL, prevC;
	next_string = "";
	i++;
	currentColumn++;
	while (size < 128) {
		if (input[i] == '\0') {
			if (debugUno) cout << "INVALIDO\n";
			errors.push(mp(currentLine, currentColumn - 1));
			return i;
		}
		else if (input[i] == '\n') {
			next_string += input[i];
			if (input[i + 1] == '\0') {
				errors.push(mp(currentLine, currentColumn));
				return (i + 1);
			}
			else {
				prevC = currentColumn;
				prevL = currentLine;
				size++;
				i++;
				currentLine++;
				currentColumn = 1;
			}
		}
		else if (input[i] == '"') {
			nodes.pb(mn("string", next_string));
			if (debugUno) cout << "VALIDO\n";
			currentColumn++;
			return (i + 1);
		}
		else {
			next_string += input[i];
			size++;
			i++;
			currentColumn++;
		}
	}
	if (input[i] != '"') {
		if (debugUno) cout << "INVALIDO\n";
		if (size > 0 && input[i - 1] == '\n') {
			errors.push(mp(prevL, prevC));
		}
		else {
			errors.push(mp(currentLine, currentColumn - 1));
		}
		return i;
	}
	else {
		nodes.pb(mn("string", next_string));
		if (debugUno) cout << "VALIDO\n";
		currentColumn++;
		return (i + 1);
	}
}

