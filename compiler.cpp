void test_parameters(node visiting) {
	if (debugTres) cout << "test_parameters\n";
	string to_declare = "";
	node args_node = mn("", "");
	for (node child : visiting.children) {
		if (child.type == "ARGS") {
			args_node = child;
		}
		else if (child.type == "identifier") {
			to_declare = child.value;
		}
	}
	if (args_node.type == "ARGS") {
		if (debugTres) cout << "has args\n";
		test_parameters(args_node);
	}
	if (to_declare != "") {
		if (debugTres) cout << "has decl: " << to_declare << endl;
		if (functions.count(to_declare)) {
			if (debugTres) cout << "function conflict\n";
			get<4>(semantic_errors) = true;
		}
		if (scope.count(to_declare)) {
			if (debugTres) cout << "var redeclaration\n";
			get<3>(semantic_errors) = true;
		}
		else {
			scope.insert(to_declare);
		}
	}
}

void test_line(node visiting) {
	if (debugTres) cout << "test_line\n";
	for (node child : visiting.children) {
		if (child.type == "C_FUNCT") {
			if (debugTres) cout << "function call\n";
			for (node grandchild : child.children) {
				if (grandchild.type == "identifier") {
					if (!functions.count(grandchild.value)) {
						if (debugTres) cout << "undeclared\n";
						get<0>(semantic_errors) = true;
					}
				}
				else if (grandchild.type == "ALT_ARGS") {
					test_line(grandchild);
				}
			}
		}
		else if (child.type == "identifier") {
			if (debugTres) cout << "id usage\n";
			if (!scope.count(child.value)) {
				if (debugTres) cout << "undeclared\n";
				get<1>(semantic_errors) = true;
			}
		}
		else {
			test_line(child);
		}
	}
}

