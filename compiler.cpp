void semantic(node visiting) {
	string function_name = "";
	node next_node = mn("", ""), code_node = mn("", ""), args_node = mn("", "");
	if (visiting.type == "FUNCT") {
		for (node child : visiting.children) {
			if (child.type == "FUNCT") {
				next_node = child;
			}
			else if (child.type == "identifier") {
				function_name = child.value;
			}
			else if (child.type == "CODE") {
				code_node = child;
			}
			else if (child.type == "ARGS") {
				args_node = child;
			}
		}
		if (next_node.type == "FUNCT") {
			semantic(next_node);
		}
		if (functions.count(function_name)) {
			get<2>(semantic_errors) = true;
		}
		else {
			functions.insert(function_name);
		}
		if (args_node.type == "ARGS") {
			test_parameters(args_node);
		}
		if (code_node.type == "CODE") {
			test_scope(code_node);
		}
		if (debugTres) {
			cout << function_name << endl << "{ ";
			for (string x : scope) {
				cout << x << " ";
			}
			cout << "}\n";
		}
		scope.clear();
	}
	else if (visiting.type == "MAIN") {
		for (node child : visiting.children) {
			if (child.type == "CODE") {
				test_scope(child);
			}
		}
		if (debugTres) {
			cout << function_name << endl << "{ ";
			for (string x : scope) {
				cout << x << " ";
			}
			cout << "}\n";
		}
	}
	else {
		for (node child : visiting.children) {
			semantic(child);
		}
	}
}