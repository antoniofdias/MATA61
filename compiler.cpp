bool input_checker(char* s, bool input_flag) {
	for (int i = 0; i < endOfFile; i++) {
		if (s[i] < 9 || s[i] > 126 || (s[i] > 10 && s[i] < 32)) {
			input_flag = true;
		}
	}
	return input_flag;
}

int next_test(int i) {
	if (i >= endOfFile) {
		return i;
	}
	else if (input[i] == '\n') {

		if (debugUno) cout << "Enter\n";
		if (debugUno) cout << currentLine << " " << currentColumn << endl;

		currentLine++;
		currentColumn = 1;
		return (i + 1);
	}
	else if (input[i] == ' ' || input[i] == '	') {

		if (debugUno && input[i] == ' ') cout << "Espaço\n";
		if (debugUno && input[i] == '	') cout << "Espaço\n";
		if (debugUno) cout << currentLine << " " << currentColumn << endl;

		currentColumn++;
		return (i + 1);
	}
	else if (reserved.count(input[i])) {
		next_string = input[i];
		nodes.pb(mn(next_string, ""));
		currentColumn++;
		return (i + 1);
	}
	else if (input[i] == '\'') {
		return test_char(i);
	}
	else if (input[i] == '"') {
		return test_string(i);
	}
	else if (input[i] >= '0' && input[i] <= '9') {
		return test_numConst(i);
	}
	else if (input[i] == '_' || (input[i] >= 'A' && input[i] <= 'Z') || (input[i] >= 'a' && input[i] <= 'z')) {
		return test_identifier(i);
	}
	else if (input[i] == '=') {
		return test_eqOp(i);
	}
	else if (input[i] == '>') {
		return test_gtOp(i);
	}
	else if (input[i] == '<') {
		return test_ltOp(i);
	}
	else if (input[i] == '!') {
		return test_df(i);
	}
	else {
		lexical_errors.push(mp(currentLine, currentColumn));
		currentColumn++;
		return (i + 1);
	}
}

int test_char(int i) {

	if (debugUno) cout << "Testando caractere\n";
	if (debugUno) cout << currentLine << " " << currentColumn << endl;

	i++;
	currentColumn++;
	if (input[i] == '\0') {
		lexical_errors.push(mp(currentLine, currentColumn - 1));
	}
	else if (input[i] == '\'') {
		lexical_errors.push(mp(currentLine, currentColumn - 1));
		return i;
	}
	else if (input[i] == '\n') {
		if (input[i + 1] == '\'') {
			nodes.pb(mn("char", "\n"));
			currentLine++;
			currentColumn = 2;
			return (i + 2);
		}
		else {
			lexical_errors.push(mp(currentLine, currentColumn));
			currentLine++;
			currentColumn = 1;
			return (i + 1);
		}
	}
	else {
		if (input[i + 1] == '\'') {
			next_string = input[i];
			nodes.pb(mn("char", next_string));
			currentColumn += 2;
			return (i + 2);
		}
		else {
			lexical_errors.push(mp(currentLine, currentColumn));
			currentColumn++;
			return (i + 1);
		}
	}
	return i;
}

